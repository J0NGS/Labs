#include "Botafogo.h"
#include "AnimaMax.h"

// ---------------------------------------------------------------------------------

Botafogo::Botafogo(TileSet * tiles)
{
    anim = new Animation(tiles, 0.060f, false);
}

// ---------------------------------------------------------------------------------

Botafogo::~Botafogo()
{
    delete anim;
}

// ---------------------------------------------------------------------------------

void Botafogo::Update()
{
    anim->NextFrame();
    if (anim->Inactive())
        AnimaMax::scene->Delete();
}

